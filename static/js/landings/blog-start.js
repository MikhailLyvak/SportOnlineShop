function renderBlogPosts() {
    for (const post of blogPosts) {
        document.write(`
        <div class="col-12 col-lg-4">
            <div class="blog-post">
                <img class="blog-img" alt="${post[currentLanguage].title}" src="${post[currentLanguage].img_src}"">
                <div class="blog-body">
                    <div class="blog-content">
                        <p class="blog-data">
                            ${post[currentLanguage].data}
                        </p>
                        <p class="blog-title">
                            ${post[currentLanguage].title}
                        </p>
                        <p class="blog-subtitle">
                            ${post[currentLanguage].subtitle}
                        </p>
                    </div>
                    <a href='${post[currentLanguage].href}'>
                        <button class="blog-button">
                            <span class="blog-button-text blog-read-more-button">Read more</span><i class="ri-arrow-right-s-line"></i>
                        </button>
                    </a>
                </div>
               
            </div>
        </div>
        `);
    }
}

function renderTitle(post) {
    document.write(`${post[currentLanguage].title}`)
}

function getTitle(post) {
    return post[currentLanguage].title;
}

function renderDate(post) {
    document.write(`${post[currentLanguage].data}`)
}

function renderSubtitle(post) {
    document.write(`${post[currentLanguage].subtitle}`)
}

function renderImage(post) {
    document.write(`<img src='${post[currentLanguage].img_src}' alt='${post[currentLanguage].title}' class='blog-post-image'>`)
}

function renderParagraphs(post) {
    post[currentLanguage].paragraphs.forEach((p) => document.write(`
        <div class="section blog-post-text-section">
            <div class="container">
                <div class="row justify-content-center">
                    <div class="col-10 blog-post-text">
                        <p>
                            ${p}
                        </p>
                    </div>
                </div>
            </div>
        </div>
    `))
}

function renderBackButton(postNum) {

    let prevNum;
    
    if (postNum === 0) {
        prevNum = blogPosts.length - 1;
    } else {
        prevNum = postNum - 1;
    }

    document.write(`
        <a href="${blogPosts[prevNum][currentLanguage].href}" class="blog-post-nav-button">
            <i class="ri-arrow-left-s-line m-0"></i>
            <span class="nav-back">Back</span>
        </a>
    `)
}

function renderForwardButton(postNum) {
    let nextNum;

    if (postNum === (blogPosts.length - 1)) {
        nextNum = 0;
    } else {
        nextNum = postNum + 1;
    }

    document.write(`
        <a href="${blogPosts[nextNum][currentLanguage].href}" class="blog-post-nav-button justify-content-end">
            <span class="nav-forward">Forward</span>
            <i class="ri-arrow-right-s-line"></i>
        </a>
    `)
}