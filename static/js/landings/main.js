window.addEventListener('scroll', function () {
    var navbar = document.getElementById('navbarMobileContainer');
    var brandLogo = document.getElementById('logoMobile');
    var menuIcon = document.getElementById('menuIcon');


    if (window.scrollY > 0) {
        navbar.style.backgroundColor = 'rgba(255, 255, 255, 0.85)';
        navbar.style.backdropFilter = 'blur(15px)';
        navbar.style.webkitBackdropFilter = 'blur(15px)';
        menuIcon.style.color = '#6836D3';
        brandLogo.src = '/static/images/landing/logo.png';
    } else {
        navbar.style.backgroundColor = 'transparent';
        navbar.style.backdropFilter = 'none';
        navbar.style.webkitBackdropFilter = 'none';
        menuIcon.style.color = '#fff';
        brandLogo.src = '/static/images/landing/logo-white.png';
    }
});