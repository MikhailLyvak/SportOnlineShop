function updateMainContent(language) {
    Object.keys(content[language]).forEach(key => {
        const element = document.getElementById(key);
        if (element) {
            if (element.tagName.toLowerCase() === 'img') {
                element.src = content[language][key];
            } else {
                if (key === 'pitch-deck-button') { 
                    element.title = (language === 'en') ? 'Download Commercial Proposition' : 'Завантажити комерційну пропозицію';
                } else {
                    element.textContent = content[language][key];
                }
            }
        }
    });
};

